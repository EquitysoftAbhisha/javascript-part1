// array push pop
// array shift unshift

let fruits = ["apple","banana","grapes"];

console.log(fruits);

// push

fruits.push("mango");

console.log(fruits);

//pop

let poppedFruit = fruits.pop();

console.log(poppedFruit);
console.log(fruits);

//unshift

fruits.unshift("mango","myfruits");

console.log(fruits);

//shift

let removedfruits = fruits.shift();

console.log(removedfruits);
console.log(fruits);